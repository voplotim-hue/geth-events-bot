function chooseFrom(list, seed, offset = 0) {
  if (!list.length) return "";
  return list[(hashText(`${seed}:${offset}`) + offset) % list.length];
}

export function normalizeBlessingTemplates(rows) {
  const normalized = rows
    .map((row) => ({
      reference: String(row.reference || "").trim(),
      verse: String(row.verse || "").trim(),
      wish: String(row.wish || "").trim(),
      is_active: String(row.is_active || "yes").trim().toLowerCase()
    }))
    .filter((row) => row.reference && row.verse && row.wish)
    .filter((row) => !["no", "false", "0", "нет"].includes(row.is_active));

  return normalized.length ? normalized : BIRTHDAY_BLESSINGS;
}

export function chooseBlessing(user, dateKey, templates = BIRTHDAY_BLESSINGS, variantIndex = null) {
  const list = templates.length ? templates : BIRTHDAY_BLESSINGS;
  if (Number.isInteger(variantIndex)) {
    return list[((variantIndex % list.length) + list.length) % list.length];
  }

  const seed = `${dateKey}:${user.telegram_user_id || user.id || user.username || ""}`;
  return list[hashText(seed) % list.length];
}

export function birthdayGreetingName(user) {
  const firstName = String(user.first_name || "").trim();
  if (firstName) return firstName;
  const username = String(user.username || "").trim();
  if (username) return `@${username}`;
  return "дорогой друг";
}

export function buildBirthdayGreeting(user, dateKey, templates, variantIndex = null) {
  const blessing = chooseBlessing(user, dateKey, templates, variantIndex);
  const name = birthdayGreetingName(user);
  const seed = `${dateKey}:${user.telegram_user_id || user.id || user.username || ""}`;
  const opening = chooseFrom(BIRTHDAY_OPENINGS, seed, 1);
  const verseBridge = chooseFrom(VERSE_BRIDGES, seed, 2);
  const reflection = chooseFrom(BIRTHDAY_REFLECTIONS, seed, 3);

  return [
    `${name}, с днем рождения!`,
    "",
    opening,
    "",
    verseBridge,
    "",
    `${blessing.reference}: "${blessing.verse}"`,
    "",
    blessing.wish,
    "",
    reflection
  ].join("\n");
}
